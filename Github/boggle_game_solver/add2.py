import sys


class ListNode:
    def __init__(self, data=0, pointer=None):
        self.val = data
        self.next = pointer


def add_2_numbers(l1: ListNode, l2: ListNode) -> ListNode:
    # Variables that create a new ListNode for the total
    head = ListNode(0, None)
    cur = head
    total = 0

    # Loop over all nodes to derive the total number
    while True:
        # Break the loop if all numbers(nodes) in the ListNode are looped
        if l1 is None and l2 is None and total == 0:
            break

        # ListNode 1
        if l1 is not None:
            total += l1.val
            l1 = l1.next

        # ListNode 2
        if l2 is not None:
            total += l2.val
            l2 = l2.next

        # Derive total numbers
        num = total % 10                          # the unit digit of the summed number
        total = total // 10                       # the tenth digit of the summed number that will be added to next loop
        cur.next = ListNode(num, None)
        cur = cur.next

    return head.next                              # the second node of the total is the start of the sequence


